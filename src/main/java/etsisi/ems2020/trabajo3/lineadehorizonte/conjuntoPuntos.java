package etsisi.ems2020.trabajo3.lineadehorizonte;

public class conjuntoPuntos {
	private Punto p1;
	private Punto p2;
	private Punto paux;
	
	public conjuntoPuntos()
	{
		p1=null;
		p2=null;
		paux=null;
	}

	public Punto getP1() {
		return p1;
	}

	public void setP1(Punto p1) {
		this.p1 = p1;
	}

	public Punto getP2() {
		return p2;
	}

	public void setP2(Punto p2) {
		this.p2 = p2;
	}

	public Punto getPaux() {
		return paux;
	}

	public void setPaux(Punto paux) {
		this.paux = paux;
	}
	
	
}
